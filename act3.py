import math
print('The floor and ceiling of 24.5 are:')
print(str(math.floor(24.5)) + ' , ' + str(math.ceil(24.5)))

x = 10
y = -15

print('The value of x after copying the sign from y is: ' + str(math.copysign(x, y)))
print('Absolute value of -96 and 56 are respectively: ' + str(math.fabs(-96)) + ' , ' + str(math.fabs(56)))

print('The GCD of 24 and 56 : ' + str(math.gcd(24, 56)))